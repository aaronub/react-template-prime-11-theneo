export default function CarouselDemo() {
    const [products, setProducts] = useState<Product[]>([]);
    const responsiveOptions: CarouselResponsiveOption[] = [
        {
            breakpoint: '1400px',
            numVisible: 2,
            numScroll: 1
        },
        {
            breakpoint: '1199px',
            numVisible: 3,
            numScroll: 1
        },
        {
            breakpoint: '767px',
            numVisible: 2,
            numScroll: 1
        },
        {
            breakpoint: '575px',
            numVisible: 1,
            numScroll: 1
        }
    ];

    const getSeverity = (product: Product) => {
        switch (product.inventoryStatus) {
            case 'INSTOCK':
                return 'success';

            case 'LOWSTOCK':
                return 'warning';

            case 'OUTOFSTOCK':
                return 'danger';

            default:
                return null;
        }
    };
    
    useEffect(() => {
        ProductService.getProductsSmall().then((data) => setProducts(data.slice(0, 9)));
    }, []);

    const productTemplate = (product: Product) => {
        return (
                <div className="border border-gray-300 rounded m-2 text-center py-5 px-3">
                    <div className="mb-3">
                        <img 
                            src={`https://primefaces.org/cdn/primereact/images/product/${product.image}`} 
                            alt={product.name} 
                            className="w-24 shadow-md mx-auto" 
                        />
                    </div>
                    <div>
                        <h4 className="mb-1 text-lg font-semibold">{product.name}</h4>
                        <h6 className="mt-0 mb-3 text-gray-600">${product.price}</h6>
                        <Tag value={product.inventoryStatus} severity={getSeverity(product)}></Tag>
                        <div className="mt-5 flex flex-wrap gap-2 justify-center">
                            <Button icon="pi pi-search" rounded />
                            <Button icon="pi pi-star-fill" rounded severity="success" />
                        </div>
                    </div>
                </div>
        );
    };
    
    return (
        <div>
            {/* Basic
            Carousel requires a collection of items as its value along with a template to render each item. */}
            <Carousel value={products} numVisible={3} numScroll={3} responsiveOptions={responsiveOptions} itemTemplate={productTemplate} />
        </div>
    )
}
        